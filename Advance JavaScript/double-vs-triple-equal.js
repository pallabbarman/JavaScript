const first = 2;
const second = "2";
if (first == second) {                 // double equal only check value
    console.log("Condition is true");
}
else {
    console.log("Condition is false");
}

const first1 = 2;
const second1 = "2";
if (first1 === second1) {               // triple equal check value + type
    console.log("Condition is true");
}
else {
    console.log("Condition is false");
}